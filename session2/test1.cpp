#include<iostream>
using namespace std;
int main(){
    //Arithmetic operations
    int a, b;
    a=2;
    b=6;
    cout<<(a+b)<<endl;
    cout<<(a-b)<<endl;
    cout<<(a*b)<<endl;
    cout<<(a/b)<<endl;
    cout<<(a%b)<<endl;
    cout<<a++<<endl;
    cout<<"a after ++ :"<<a<<endl;
    cout<<b--<<endl;
    return 0;
}